const turn = fromLittleEndian("3fa51fd0a8bb5298");

function xorBuffers(buf1: Buffer, buf2: Buffer) {
  const result = Buffer.alloc(8);
  for (let i = 0; i < 8; i++) {
    result[i] = buf1[i] ^ buf2[i];
  }
  return result;
}

function handIndex(pieceType: PieceType): number {
  switch (pieceType) {
    case PieceType.PAWN:
      return 1;
    case PieceType.LANCE:
      return 2;
    case PieceType.KNIGHT:
      return 3;
    case PieceType.SILVER:
      return 4;
    case PieceType.GOLD:
      return 5;
    case PieceType.BISHOP:
      return 6;
    case PieceType.ROOK:
      return 7;
    default:
      throw new Error("Invalid piece type");
  }
}

export function hash(hash: string): string {
  const position = Position.newBySFEN(hash);
  if (!position) {
    throw new Error("Invalid SFEN");
  }
  let result = Buffer.alloc(8);
  for (const square of Square.all) {
    const piece = position.board.at(square);
    if (!piece) {
      continue;
    }
    const squareIndex = toAperySquare(square);
    const pieceIndex = toAperyPiece(piece);
    result = xorBuffers(result, getPieceTable()[pieceIndex * 81 + squareIndex]);
  }
  for (const pieceType of handPieceTypes) {
    const n = position.hand(position.color).count(pieceType);
    const pieceIndex = handIndex(pieceType);
    result = xorBuffers(result, getHandTable()[(pieceIndex - 1) * 19 + n]);
  }
  if (position.color === Color.WHITE) {
    result = xorBuffers(result, turn);
  }
  return result.toString("hex");
}
